def parse_sat_model(input_data):
    parsed_data = defaultdict(dict)
    for line in input_data.strip().splitlines():
        if "->" in line:
            key, value = line.split("->")
            state, var = key.split("@")
            parsed_data[int(var.strip())][state.strip()] = value.strip()

    result = []
    for state, variables in sorted(parsed_data.items()):
        result.append(f"\nState {state}:")
        for var, val in sorted(variables.items()):
            result.append(f"{var} = {val}")
    return result

